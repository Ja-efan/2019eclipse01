package Break;

public class Switch {
	
	public static void main(String[] args)
	{
		char a = 'a';
		
		switch(a)
		{
			case'a':
				System.out.println("a 입니다.");
			break;
			
			case'b':
				System.out.println("b 입니다.");
			break;
			
			case'c':
				System.out.println("c 입니다.");
			break;
		}
	}

}
