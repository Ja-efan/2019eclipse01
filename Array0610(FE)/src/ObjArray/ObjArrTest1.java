package ObjArray;

public class ObjArrTest1 {
	
	String name;
	int score;
	
	public void setName (String tmpName) { // 이름입력
		name = tmpName;
	}
	
	public String getName () { // 이름반환
		return name;
	}
	
	public void setScore (int tmpScore) { // 점수입력
		score = tmpScore;
	}
	
	public int getScore () { // 점수반환
		return score;
	}
	
	

}
